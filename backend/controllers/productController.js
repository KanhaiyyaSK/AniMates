// Create New Review or Update the review
exports.createProductReview = catchAsyncErrors(async (req, res, next) => {
  const { rating, comment, productId } = req.body;

  const review = {
    user: req.user._id,
    name: req.user.name,
    // rating ko number mein convert karo
    rating: Number(rating),
    comment,
  };

  // find product
  const product = await Product.findById(productId);

  // user jo review karna chahta hai uski id agar same hai any of user._id in out bd then usne pehle review kara hua hai else nahi karra
  const isReviewed = product.reviews.find(
    (rev) => rev.user.toString() === req.user._id.toString()
  );

  // if user ne pehle he review karra hua hai toh uske review ko update karna hai
  if (isReviewed) {
    product.reviews.forEach((rev) => {
      // if user id given by user and
      if (rev.user.toString() === req.user._id.toString())
        (rev.rating = rating), (rev.comment = comment);
    });
  } else {
    // simple agar first time user review karra to directly user ke review ko review array mein push kardo
    product.reviews.push(review);
    product.numOfReviews = product.reviews.length;
  }

  let avg = 0;

  product.reviews.forEach((rev) => {
    //sum of all rating
    avg += rev.rating;
  });

  // finally finding average by dividing the reviews.length
  product.ratings = avg / product.reviews.length;

  // after all the updates save the changes in the db
  await product.save({ validateBeforeSave: false });

  res.status(200).json({
    success: true,
  });
});

